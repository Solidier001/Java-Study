package creational.builder;

public class House {
    private String baies;
    private String wall;
    private String roof;

    public String getBaies() {
        return baies;
    }

    public void setBaies(String baies) {
        this.baies = baies;
    }

    public String getWall() {
        return wall;
    }

    public void setWall(String wall) {
        this.wall = wall;
    }

    public String getRoof() {
        return roof;
    }

    public void setRoof(String roof) {
        this.roof = roof;
    }
}
